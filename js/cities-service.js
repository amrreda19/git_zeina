    // الحصول على المدن لمحافظة معينة
    getCitiesForGovernorate(governorate) {
        return this.citiesData[governorate] || [];
    }

    // الحصول على جميع المحافظات
    getAllGovernorates() {
        return Object.keys(this.citiesData);
    }

    // التحقق من وجود محافظة
    hasGovernorate(governorate) {
        return this.citiesData.hasOwnProperty(governorate);
    }

    // الحصول على عدد المدن في محافظة معينة
    getCitiesCount(governorate) {
        const cities = this.getCitiesForGovernorate(governorate);
        return cities.length;
    }

    // البحث في المدن
    searchCities(governorate, searchTerm) {
        const cities = this.getCitiesForGovernorate(governorate);
        if (!searchTerm) return cities;
        
        return cities.filter(city => 
            city.toLowerCase().includes(searchTerm.toLowerCase())
        );
    }
